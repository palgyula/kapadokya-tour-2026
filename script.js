function drawPreviewPoint(latlng) {
  if (!state.drawingPreviewLayer) {
    state.drawingPreviewLayer = L.layerGroup().addTo(state.map);
  }

  state.drawingPreviewLayer.clearLayers();
  const coords = state.drawingPoints.map((point) => [point.lat, point.lng]);
  state.drawingPoints.forEach((point) => {
    L.circleMarker([point.lat, point.lng], {
      radius: 4,
      color: state.mapConfig.shapeColor,
      fillColor: state.mapConfig.shapeColor,
      fillOpacity: 0.9,
    }).addTo(state.drawingPreviewLayer);
  });

  if (coords.length > 1) {
    L.polyline(coords, {
      color: state.mapConfig.shapeColor,
      weight: state.mapConfig.lineWeight,
      dashArray: "6 6",
    }).addTo(state.drawingPreviewLayer);
  }
}

function finishDrawing() {
  if (!requireMapManagerAccess() || !state.drawingMode) {
    return;
  }

  const minimumPoints = state.drawingMode === "polygon" ? 3 : 2;
  if (state.drawingPoints.length < minimumPoints) {
    showMapStatus(t("validationShapePoints"), true);
    return;
  }

  const nameInput = document.getElementById("shapeNameInput");
  const shape = {
    id: `shape-${Date.now()}`,
    layerId: getActiveConfigLayer().id,
    type: state.drawingMode,
    name: nameInput.value.trim() || t("shapeNameLabel"),
    points: state.drawingPoints,
    color: state.mapConfig.shapeColor,
    weight: Number(state.mapConfig.lineWeight) || 4,
  };

  state.mapConfig.shapes.push(shape);
  nameInput.value = "";
  cancelDrawing(false);
  saveEditorState();
  populateShapeList();
  renderConfigShapes();
  syncConfigControls();
  showMapStatus(t("actionShapeSaved"));
}

function cancelDrawing(withMessage = true) {
  state.drawingMode = null;
  state.drawingPoints = [];
  if (state.drawingPreviewLayer && state.map) {
    state.map.removeLayer(state.drawingPreviewLayer);
  }
  state.drawingPreviewLayer = null;
  if (withMessage) {
    showMapStatus(t("actionDrawingCancelled"));
  }
}

