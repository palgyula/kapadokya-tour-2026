function renderDayMarkers(day) {
  if (!day) {
    return;
  }

  getMarkersForDay(day.id).forEach((marker) => {
    const note = state.lang === "hu" ? marker.noteHu : marker.noteEn;
    let layer;

    if (marker.source === "route") {
      const isHotel = marker.title.includes("Uygulama Hotel");
      if (isHotel) {
        layer = L.circleMarker([marker.lat, marker.lng], {
          radius: 6,
          color: "#990000",
          fillColor: "#ff4d4d",
          fillOpacity: 0.95,
          weight: 2,
        });
      } else {
        layer = L.marker([marker.lat, marker.lng]);
      }
    } else {
      layer = L.circleMarker([marker.lat, marker.lng], {
        radius: 7,
        color: state.mapConfig.markerColor,
        fillColor: state.mapConfig.markerColor,
        fillOpacity: 0.95,
        weight: 2,
      });
    }

    layer.addTo(state.markersLayer).bindPopup(`
        <strong>${buildMarkerTitle(marker)}</strong><br>
        ${marker.time ? `${marker.time}<br>` : ""}
        ${note}<br>
        ${t("markerTypeLabel")}: ${
          marker.source === "route"
            ? t("markerRouteLabel")
            : t("markerCustomLabel")
        }
      `);
  });
}

function populateMarkerList() {
  const dayId = state.selectedDayId;
  const list = document.getElementById("markerList");

  if (!dayId) {
    list.innerHTML = `<div class="marker-item"><p>${t("markerNoDaySelected")}</p></div>`;
    return;
  }

  const markers = getMarkersForDay(dayId);
  if (!markers.length) {
    list.innerHTML = `<div class="marker-item"><p>${t("markerListEmpty")}</p></div>`;
    return;
  }

  list.innerHTML = markers
    .map((marker) => {
      const note = state.lang === "hu" ? marker.noteHu : marker.noteEn;
      const sourceLabel =
        marker.source === "route"
          ? t("markerRouteLabel")
          : t("markerCustomLabel");
      const actions =
        marker.source === "custom"
          ? `<div class="marker-form__actions"><button class="button button--secondary" type="button" data-edit-marker-id="${marker.id}">${t("markerEditButton")}</button><button class="button button--danger" type="button" data-delete-marker-id="${marker.id}">${t("markerDeleteButton")}</button></div>`
          : `<div class="marker-form__actions"><button class="button button--secondary" type="button" data-edit-marker-id="${marker.id}">${t("markerEditButton")}</button></div>`;

      return `
        <div class="marker-item">
          <strong>${buildMarkerTitle(marker)}</strong>
          <small>${marker.time || t("markerTimeUnknown")} • ${sourceLabel}</small>
          <p>${note || t("markerNoDescription")}</p>
          ${actions}
        </div>
      `;
    })
    .join("");
}

function openMarkerForm(marker = null, latlng = null) {
  if (!requireMapManagerAccess()) {
    return;
  }

  const form = document.getElementById("markerForm");
  const titleInput = document.getElementById("markerTitle");
  const descriptionInput = document.getElementById("markerDescription");
  const timeInput = document.getElementById("markerTime");
  const photosInput = document.getElementById("markerPhotos");
  const coords = document.getElementById("markerCoords");
  const deleteBtn = document.getElementById("deleteMarkerBtn");
  const hint = document.getElementById("markerPanelHint");

  state.markerFormActive = true;
  form.classList.remove("hidden");
  form.setAttribute("aria-hidden", "false");

  if (marker) {
    state.editingMarkerId = marker.id;
    titleInput.value = marker.title;
    descriptionInput.value =
      state.lang === "hu" ? marker.noteHu : marker.noteEn;
    timeInput.value = marker.time || "";
    photosInput.value = marker.photos || "";
    coords.textContent = `${marker.lat.toFixed(5)}, ${marker.lng.toFixed(5)}`;
    deleteBtn.classList.toggle("hidden", marker.source === "route");
    hint.textContent = t("markerFormEditHint");

    state.pendingMarker = {
      dayId: marker.dayId,
      lat: marker.lat,
      lng: marker.lng,
      source: marker.source,
    };
  } else if (latlng) {
    state.editingMarkerId = null;
    titleInput.value = "";
    descriptionInput.value = "";
    timeInput.value = "";
    photosInput.value = "";
    coords.textContent = `${latlng.lat.toFixed(5)}, ${latlng.lng.toFixed(5)}`;
    deleteBtn.classList.add("hidden");
    hint.textContent = t("markerFormCreateHint");

    state.pendingMarker = {
      dayId: state.selectedDayId,
      lat: latlng.lat,
      lng: latlng.lng,
      source: "custom",
    };
  }
}

