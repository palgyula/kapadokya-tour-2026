function renderAllDayRoutes() {
  const day = trip.days.find((tripDay) => tripDay.id === "2026-06-21");
  if (!day) {
    return;
  }

  const boundsSource = [];
  state.allModeLayer = L.layerGroup().addTo(state.map);

  const visibleStops = day.route;
  const flightCoords = visibleStops
    .filter((stop) => stop.road === false)
    .map((stop) => [stop.lat, stop.lng]);
  const roadCoords = visibleStops
    .filter((stop) => stop.road !== false)
    .map((stop) => [stop.lat, stop.lng]);

  visibleStops.forEach((stop, index) => {
    const note = state.lang === "hu" ? stop.noteHu : stop.noteEn;
    const isHotel = stop.place.includes("Uygulama Hotel");
    const markerOptions = isHotel
      ? {
          radius: 6,
          color: "#990000",
          fillColor: "#ff4d4d",
          fillOpacity: 0.95,
          weight: 2,
        }
      : {
          radius: 4,
          color: "#5c341f",
          fillColor: "#d2a06f",
          fillOpacity: 0.9,
          weight: 1,
        };
    L.circleMarker([stop.lat, stop.lng], markerOptions)
      .addTo(state.allModeLayer)
      .bindPopup(
        `<strong>${day.id} ${stop.time}</strong><br>${stop.place}<br>${note}<br>${t("mapStop")} ${index + 1}`,
      );
  });

  const budapestStop = visibleStops.find((stop) =>
    stop.place.includes("Budapest"),
  );
  const istanbulStop = visibleStops.find((stop) =>
    stop.place.includes("Istanbul"),
  );
  const kayseriStop = visibleStops.find((stop) =>
    stop.place.includes("Kayseri"),
  );
  if (budapestStop && istanbulStop) {
    L.polyline(
      [
        [budapestStop.lat, budapestStop.lng],
        [istanbulStop.lat, istanbulStop.lng],
      ],
      {
        color: "#2d74da",
        weight: 3,
        opacity: 0.85,
        dashArray: "10 8",
      },
    ).addTo(state.allModeLayer);
  }
  if (istanbulStop && kayseriStop) {
    L.polyline(
      [
        [istanbulStop.lat, istanbulStop.lng],
        [kayseriStop.lat, kayseriStop.lng],
      ],
      {
        color: "#2d74da",
        weight: 3,
        opacity: 0.85,
        dashArray: "10 8",
      },
    ).addTo(state.allModeLayer);
  }

  if (roadCoords.length > 1) {
    if (L.Routing && L.Routing.control) {
      const control = L.Routing.control({
        waypoints: roadCoords.map((coord) => L.latLng(coord[0], coord[1])),
        lineOptions: {
          styles: [
            {
              color: state.mapConfig.routeColor,
              weight: state.mapConfig.lineWeight,
              opacity: 0.78,
            },
          ],
        },
        addWaypoints: false,
        fitSelectedRoutes: false,
        routeWhileDragging: false,
        show: false,
        createMarker: () => null,
      }).addTo(state.map);
      state.allRoutingControls.push(control);
    } else {
      L.polyline(roadCoords, {
        color: state.mapConfig.routeColor,
        weight: state.mapConfig.lineWeight,
        opacity: 0.8,
      }).addTo(state.allModeLayer);
    }
  }

  boundsSource.push(...flightCoords, ...roadCoords);
  if (boundsSource.length > 1) {
    state.map.fitBounds(boundsSource, { padding: [24, 24] });
  }
}

function renderSelectedDayRoute() {
  const day = currentDay();

  state.markersLayer.clearLayers();
  if (state.routeLayer) {
    state.map.removeLayer(state.routeLayer);
    state.routeLayer = null;
  }
  if (state.routingControl) {
    state.map.removeControl(state.routingControl);
    state.routingControl = null;
  }
  if (state.flightLayer) {
    state.map.removeLayer(state.flightLayer);
    state.flightLayer = null;
  }
  if (state.busLayer) {
    state.map.removeLayer(state.busLayer);
    state.busLayer = null;
  }
  if (state.allModeLayer) {
    state.map.removeLayer(state.allModeLayer);
    state.allModeLayer = null;
  }
  if (state.customRouteLayer) {
    state.map.removeLayer(state.customRouteLayer);
    state.customRouteLayer = null;
  }
  if (state.allRoutingControls.length) {
    state.allRoutingControls.forEach((control) =>
      state.map.removeControl(control),
    );
    state.allRoutingControls = [];
  }

  if (!day || day.id === "2026-06-21") {
    renderAllDayRoutes();
    return;
  }

  const routeStops = day.route.filter((stop) => stop.road !== false);
  const routeCoords = routeStops.map((stop) => [stop.lat, stop.lng]);

  if (routeCoords.length > 1) {
    if (L.Routing && L.Routing.control) {
      state.routingControl = L.Routing.control({
        waypoints: routeCoords.map((coord) => L.latLng(coord[0], coord[1])),
        lineOptions: {
          styles: [
            {
              color: state.mapConfig.routeColor,
              weight: state.mapConfig.lineWeight,
              opacity: 0.78,
            },
          ],
        },
        addWaypoints: false,
        fitSelectedRoutes: false,
        routeWhileDragging: false,
        show: false,
        createMarker: () => null,
      }).addTo(state.map);
    } else {
      state.routeLayer = L.polyline(routeCoords, {
        color: state.mapConfig.routeColor,
        weight: state.mapConfig.lineWeight,
        opacity: 0.8,
      }).addTo(state.map);
    }
  }

  renderDayMarkers(day);
  renderCustomRouteLines(day);

  const visibleCoords = day.route.map((stop) => [stop.lat, stop.lng]);
  if (visibleCoords.length > 1) {
    state.map.fitBounds(visibleCoords, { padding: [24, 24] });
  } else if (visibleCoords.length === 1) {
    state.map.setView(visibleCoords[0], 12);
  }
}

