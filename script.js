function renderConfigShapes() {
  if (!state.map || !state.configLayerGroup) {
    return;
  }

  state.configLayerGroup.clearLayers();
  if (!state.loggedIn) {
    return;
  }

  const visibleLayers = new Set(
    state.mapConfig.layers
      .filter((layer) => layer.visible)
      .map((layer) => layer.id),
  );

  state.mapConfig.layers.forEach((layer) => {
    state.mapConfig.shapes
      .filter((shape) => shape.layerId === layer.id && visibleLayers.has(layer.id))
      .forEach((shape) => {
        const coords = shape.points.map((point) => [point.lat, point.lng]);
        const options = {
          color: shape.color || state.mapConfig.shapeColor,
          weight: Number(shape.weight) || Number(state.mapConfig.lineWeight) || 4,
          fillOpacity: shape.type === "polygon" ? 0.16 : 0,
        };
        const leafletShape =
          shape.type === "polygon"
            ? L.polygon(coords, options)
            : L.polyline(coords, options);
        leafletShape
          .addTo(state.configLayerGroup)
          .bindPopup(`<strong>${shape.name}</strong><br>${layer.name}`);
      });
  });
}

