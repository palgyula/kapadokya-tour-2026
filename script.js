function buildMediaEntries() {
  const bowlingFiles = localPhotoFiles.filter((file) =>
    file.includes("bowling_"),
  );
  const normalPhotoFiles = localPhotoFiles.filter(
    (file) => !file.includes("bowling_"),
  );

  const sortedPhotoFiles = [...normalPhotoFiles].sort((left, right) => {
    const dateLeft = photoDateByFile[left] || "9999-12-31";
    const dateRight = photoDateByFile[right] || "9999-12-31";
    if (dateLeft !== dateRight) {
      return dateLeft.localeCompare(dateRight);
    }
    return left.localeCompare(right);
  });

  const dateIndexByFile = {};
  const dateCounts = {};
  sortedPhotoFiles.forEach((file) => {
    const captureDate = photoDateByFile[file] || "";
    dateCounts[captureDate] = (dateCounts[captureDate] || 0) + 1;
    dateIndexByFile[file] = dateCounts[captureDate];
  });

  const photos = sortedPhotoFiles.map((file, idx) => {
    const index = idx + 1;
    const captureDate = photoDateByFile[file] || "";
    const dayIndex = dateIndexByFile[file] || 0;
    const story = storyForIndex(index, file, dayIndex);
    const group = getProgramGroup(index, file, dayIndex);
    return {
      type: "photo",
      src: toJpgPath(file),
      originalSrc: file,
      date: captureDate,
      captionEn: story.captionEn,
      captionHu: story.captionHu,
      groupId: group.id,
      groupLabelEn: group.labelEn,
      groupLabelHu: group.labelHu,
      // Calendar/day grouping follows the real EXIF capture date so photos
      // land on the day they were actually taken, not a narrative program day.
      dayId: captureDate || null,
    };
  });

  const bowlingEntries = bowlingFiles.sort().map((file) => {
    const captureDate = photoDateByFile[file] || "2026-07-02";
    return {
      type: "photo",
      src: toJpgPath(file),
      originalSrc: file,
      date: captureDate,
      captionEn: "Bowling night photos",
      captionHu: "Bowling éjszaka fotói",
      groupId: "bowling",
      groupLabelEn: "Bowling Night",
      groupLabelHu: "Bowling este",
      dayId: captureDate || null,
    };
  });

  const photosWithBowling = [...photos, ...bowlingEntries];

  const videos = localVideoFiles.map((file) => ({
    type: "video",
    src: file,
    date: "",
    captionEn: `Video - ${file.split("/").pop()}`,
    captionHu: `Videó - ${file.split("/").pop()}`,
    groupId: "videos",
    groupLabelEn: "Videos",
    groupLabelHu: "Videók",
    dayId: null,
  }));

  return [...photosWithBowling, ...videos];
}

