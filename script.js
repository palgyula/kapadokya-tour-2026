function updateLayer(layerId, updates) {
  if (!requireMapManagerAccess()) {
    return;
  }

  const layer = state.mapConfig.layers.find((item) => item.id === layerId);
  if (!layer) {
    return;
  }

  Object.assign(layer, updates);
  saveEditorState();
  populateLayerList();
  renderConfigShapes();
  showMapStatus(t("actionLayerUpdated"));
}

function deleteLayer(layerId) {
  if (!requireMapManagerAccess()) {
    return;
  }

  const layer = state.mapConfig.layers.find((item) => item.id === layerId);
  if (!layer || layer.locked) {
    return;
  }

  state.mapConfig.layers = state.mapConfig.layers.filter(
    (item) => item.id !== layerId,
  );
  state.mapConfig.shapes = state.mapConfig.shapes.filter(
    (shape) => shape.layerId !== layerId,
  );
  state.activeLayerId = "base";
  saveEditorState();
  populateLayerList();
  populateShapeList();
  renderConfigShapes();
  syncConfigControls();
  showMapStatus(t("actionLayerDeleted"));
}

function moveLayer(layerId, direction) {
  if (!requireMapManagerAccess()) {
    return;
  }

  const layers = state.mapConfig.layers;
  const index = layers.findIndex((layer) => layer.id === layerId);
  const nextIndex = index + direction;
  if (index < 0 || nextIndex < 0 || nextIndex >= layers.length) {
    return;
  }

  [layers[index], layers[nextIndex]] = [layers[nextIndex], layers[index]];
  saveEditorState();
  populateLayerList();
  renderConfigShapes();
  showMapStatus(t("actionLayerUpdated"));
}

